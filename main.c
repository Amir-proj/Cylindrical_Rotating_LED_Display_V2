static void esp_spp_cb(esp_spp_cb_event_t event,esp_spp_cb_param_t *param)
{ /*
in funtion there are two parameters event and *param event is for using
if else statment to keep letting event handler know which event is occuring by 
updating values in background and param provides us information about device 
we are connected to which is phone. param is responsible for sending and receiving data*/
my_handle = param->write.handle; /*we declared my_handle above now we are saving 
write handle values so it saves the writing address while writing data in separate funtion*/    
switch (event){ //using switch case cause we need to use if statements
case ESP_SPP_INIT_EVT: //if event matches enumerated value
  if(param->init.status == ESP_SPP_SUCCESS){ //esp_spp_status enum if is equal to 0
  ESP_LOGI(TAG1,"ESP_SPP_INIT_EVNT"); //log value shows this event
  esp_spp_start_srv(sec_mask, role_slave, 0, SPP_SERVER_NAME); /*for starting we declare
  security method and role of esp32 and its server name*/
}
else {
              ESP_LOGE(TAG1, "ESP_SPP_INIT_EVT status:%d", param->init.status);
          }//incase of failure it prints status value
      break;
case ESP_SPP_DISCOVERY_COMP_EVT:
     ESP_LOGI(TAG1, "ESP_SPP_DISCOVERY_COMP_EVT"); //discovery event occuring
      break;
case ESP_SPP_OPEN_EVT:
        ESP_LOGI(TAG1, "ESP_SPP_OPEN_EVT"); //event is open for connections
        if (num_connections < 3) {
                num_connections++;
            } else {
                ESP_LOGE(TAG1, "Max connections reached. Rejecting new connection.");
                esp_spp_disconnect(param->open.handle); /* if connections reaches more than 
required connections it will disconnect*/
            }
        break;
case ESP_SPP_CLOSE_EVT:
        ESP_LOGI(TAG1, "ESP_SPP_CLOSE_EVT status:%d handle:%ld close_by_remote:%d", param->close.status,
                 param->close.handle, param->close.async);
        break; /*when esp diconnects this event closes*/
case ESP_SPP_START_EVT: // esp spp event when starts
        if (param->start.status == ESP_SPP_SUCCESS) {
            ESP_LOGI(TAG1, "ESP_SPP_START_EVT handle:%ld sec_id:%d scn:%d", param->start.handle, param->start.sec_id,
                     param->start.scn);
            esp_bt_gap_set_device_name(DEVICE_NAME); //device name goes here
            esp_bt_gap_set_scan_mode(ESP_BT_CONNECTABLE, ESP_BT_GENERAL_DISCOVERABLE);/*
 This means that the Bluetooth device will be connectable, allowing other Bluetooth devices to establish connections with it.
 This means that the Bluetooth device will be discoverable, allowing other devices to find it during the discovery process.*/
        } else {
            ESP_LOGE(TAG1, "ESP_SPP_START_EVT status:%d", param->start.status);
        }
        break;
case ESP_SPP_CL_INIT_EVT:
        ESP_LOGI(TAG1, "ESP_SPP_CL_INIT_EVT");//when the SPP client is initialized,
        break;
case ESP_SPP_DATA_IND_EVT: //if event data print value received
        uint8_t* receive = param->data_ind.data;

            if(receive[0] == 'r')
            {
                for(int i = 1;i < 501; i++)
                {
                    buffer_red[red_ind] = receive[i];
                    red_ind++;
                }
                if (red_ind >= 2499)
                {
                    red_ind = 0;
                    red_done = true;
                }
            }
            else if(receive[0] == 'b')
            {
                for(int i = 1;i < 501; i++)
                {
                    buffer_blue[blue_ind] = receive[i];
                    blue_ind++;
                }
                if (blue_ind >= 2499)
                {
                    blue_ind = 0;
                    blue_done = true;
                }
            }
            else if(receive[0] == 'g')
            {
                for(int i = 1;i < 501; i++)
                {
                    buffer_green[green_ind] = receive[i];
                    green_ind++;
                }
                if (green_ind >= 2499)
                {
                    green_ind = 0;
                    green_done = true;
                }
            }
            else if(receive[0] == 's')
            {
                mode_slideshow = true;
                mode_BT = false;
            }
            else if(receive[0] == 'w')
            {
                mode_slideshow = false;
                mode_BT = true;
            }
            
    break;
    case ESP_SPP_WRITE_EVT:
        ESP_LOGI(TAG1, "ESP_SPP_WRITE_EVT"); // when we write from esp32 to mobile phone
        break;
    default:
        break;    
}
}

