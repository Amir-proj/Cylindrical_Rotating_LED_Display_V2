void app_main() 
{
    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = SPI_MASTER_FREQ_20M, // Clock out at 11 MHz
        .mode = 0,                 // SPI mode 0 - the clock signal starts with a low signal
        .spics_io_num = 5,        // CS pin
        .queue_size = 7,           // Queue 7 transactions at a time
        .command_bits = 0,
        .address_bits = 0,
        .dummy_bits = 0,
    };

    ESP_LOGI(TAG, "Initializing VSPI"); // SPI3 = VSPI
    spi_bus_config_t buscfg = {
        .miso_io_num = -1,
        .mosi_io_num = 23,
        .sclk_io_num = 18,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = 1024,
    };

    // Initialize the SPI
    spi_bus_initialize(VSPI_HOST, &buscfg, SPI_DMA_CH_AUTO);

    // Define SPI handle
    spi_device_handle_t spi;
    
    spi_bus_add_device(VSPI_HOST, &devcfg, &spi);

    esp_rom_gpio_pad_select_gpio(INPUT);

    gpio_set_direction(INPUT, GPIO_MODE_INPUT);
    
    gpio_set_intr_type(INPUT,GPIO_INTR_NEGEDGE);
    gpio_install_isr_service(0);
    gpio_isr_handler_add(INPUT, position_callback, NULL);
    gpio_intr_enable(INPUT);


    const esp_timer_create_args_t my_timer_args = {
        .callback = &timer_callback,
        .name = "My Timer",
        .arg = (void*) spi};
    esp_timer_create(&my_timer_args, &timer_handler);
    previous_time = esp_timer_get_time();
    ESP_LOGI(TAG, "%lld", previous_time);

    for (int i = 0; i < 2; i++) {
        display_line[i] = (uint8_t *)heap_caps_malloc(215, MALLOC_CAP_DMA);
        if (!display_line[i]) {
            ESP_LOGE(TAG, "Failed to allocate DMA buffer");
            return;
        }
    }

    for (int i = 0; i < 2; i++) {
        display_line2[i] = (uint8_t *)heap_caps_malloc(215, MALLOC_CAP_DMA);
        if (!display_line2[i]) {
            ESP_LOGE(TAG, "Failed to allocate DMA buffer");
            return;
        }
    }


    nvs_flash_erase();
    nvs_flash_init(); //it will save important details necesasry for run time program
    esp_bt_controller_config_t bt_cfg = BT_CONTROLLER_INIT_CONFIG_DEFAULT(); //bt config default
    if (esp_bt_controller_init(&bt_cfg) != ESP_OK)
        printf( "Bluetooth controller initialize failed\n");
    else  
        printf( "BT init succeess\n" );

    esp_bt_controller_enable(ESP_BT_MODE_BTDM); //enabling classic bluetooth mode
    esp_bluedroid_init(); //initialize bluedroid
    esp_bluedroid_enable(); //enable bluedroid
    
    esp_spp_register_callback(esp_spp_cb); //event handler registerd here
    esp_spp_init(esp_spp_mode);
    

    xTaskCreatePinnedToCore(&Render_line, "Render_line", configMINIMAL_STACK_SIZE, NULL, 5, &display_task,0);
    xTaskCreatePinnedToCore(&Render_line2, "Render_line", configMINIMAL_STACK_SIZE, NULL, 5, &display_task2,0);
    xTaskCreatePinnedToCore(&BT_func, "BT_func", 10000, NULL, 5,NULL,1);

}

